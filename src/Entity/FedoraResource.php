      ->setSetting('handler', 'default')
      ->setDefaultValueCallback('Drupal\islandora\Entity\FedoraResource::getFedoraRoot')
      ->setTranslatable(TRUE)
      ->setDisplayOptions('view', array(
        'label' => 'hidden',
